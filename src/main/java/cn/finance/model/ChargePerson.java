package cn.finance.model;

import cn.finance.model.base.PersistentObject;

/**
 *  客户方负责人表
 */
public class ChargePerson extends PersistentObject {

	private static final long serialVersionUID = 1L;
	
	/**
	 * 客户
	 */
	private ContractClient contractClient;
	
	/**
	 * 用户类型
	 * 常规联系人      1
	 * 财务联系人      2
	 * 发票联系人      3
	 */
	private Integer userType;
	
	/**
	 * 负责人姓名
	 */
	private String name;    
	
	/**
	 * 电话
	 */
	private String phone;
	
	/**
	 * 地址
	 */
	private String address;
	
	/**
	 * email 
	 */
	private String email;
	
	/**
	 * 传真
	 */
	private String fax;
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getFax() {
		return fax;
	}

	public void setFax(String fax) {
		this.fax = fax;
	}

	public ContractClient getContractClient() {
		return contractClient;
	}

	public void setContractClient(ContractClient contractClient) {
		this.contractClient = contractClient;
	}

	public Integer getUserType() {
		return userType;
	}

	public void setUserType(Integer userType) {
		this.userType = userType;
	}
	
	
	
}
