package cn.finance.model;

import cn.finance.model.base.PersistentObject;

/**
 *  项目表
 */
public class ContractProject extends PersistentObject {

	private static final long serialVersionUID = 1L;
	
	/**
	 * 客户
	 */
	private ContractClient contractClient;
	
	/**
	 * 业务种类
	 */
	private BusinessType businessType;
	
	/**
	 * 项目名
	 */
	private String name;  
	
	/**
	 * 属于的行业
	 */
	private Industry industry;
	
	/**
	 * 达闻公司名
	 */
	private DarwinName darwinName;

	private Bd bd;

	private Pm pm;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public ContractClient getContractClient() {
		return contractClient;
	}

	public void setContractClient(ContractClient contractClient) {
		this.contractClient = contractClient;
	}

	public Industry getIndustry() {
		return industry;
	}

	public void setIndustry(Industry industry) {
		this.industry = industry;
	}

	public Bd getBd() {
		return bd;
	}

	public void setBd(Bd bd) {
		this.bd = bd;
	}

	public Pm getPm() {
		return pm;
	}

	public void setPm(Pm pm) {
		this.pm = pm;
	}

	public BusinessType getBusinessType() {
		return businessType;
	}

	public void setBusinessType(BusinessType businessType) {
		this.businessType = businessType;
	}

	public DarwinName getDarwinName() {
		return darwinName;
	}

	public void setDarwinName(DarwinName darwinName) {
		this.darwinName = darwinName;
	}
	
}
